import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

// ---------------------------------------------------------------------------
// mock-paynow-result
// ---------------------------------------------------------------------------
// This Edge Function simulates a payment gateway callback for the demo/test
// environment. It is intentionally reachable without a JWT (verify_jwt=false
// in config.toml) because in the real Paynow integration, the callback also
// comes from an external server without a JWT.
//
// Security model:
//  - simulationToken   : short-lived UUID tied to this specific payment row,
//                        generated by create-checkout, returned to the client,
//                        stored in the client's sessionStorage. Without it,
//                        nobody can mutate payment state.
//  - payment_provider  : must be 'mock' – real Paynow rows are untouchable.
//  - is_test           : must be true  – production orders are untouchable.
//  - status            : must be PENDING – strict one-way state transitions.
//  - PAYMENT_PROVIDER  : if set to 'paynow' in env vars, this endpoint is
//                        fully disabled (returns 403).
//
// When you integrate real Paynow:
//  - Create paynow-webhook/index.ts for the real callback (validates Paynow hash)
//  - Set PAYMENT_PROVIDER=paynow env var to disable this mock endpoint
//  - This file can be deleted or kept for local dev/testing
// ---------------------------------------------------------------------------

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    // Guard: disable entirely if running against real Paynow in production
    if (Deno.env.get('PAYMENT_PROVIDER') === 'paynow') {
      return new Response(
        JSON.stringify({ error: 'Mock payment simulation is disabled in production Paynow mode.' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 403 }
      )
    }

    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    )

    const { orderId, paymentId, status, simulationToken } = await req.json()

    // ------------------------------------------------------------------
    // 1. Basic input validation
    // ------------------------------------------------------------------
    if (!orderId || !paymentId || !simulationToken) {
      return new Response(
        JSON.stringify({ error: 'orderId, paymentId, and simulationToken are required.' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 400 }
      )
    }

    const allowedStatuses = ['PAID', 'FAILED', 'EXPIRED']
    if (!allowedStatuses.includes(status)) {
      return new Response(
        JSON.stringify({ error: `Invalid status "${status}". Must be one of: ${allowedStatuses.join(', ')}` }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 400 }
      )
    }

    // ------------------------------------------------------------------
    // 2. Fetch the payment row and verify all guards
    // ------------------------------------------------------------------
    const { data: payment, error: fetchError } = await supabaseClient
      .from('payments')
      .select('id, status, is_test, simulation_token, payment_provider')
      .eq('id', paymentId)
      .single()

    if (fetchError || !payment) {
      return new Response(
        JSON.stringify({ error: 'Payment not found.' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 404 }
      )
    }

    // Guard: only test payments
    if (!payment.is_test) {
      return new Response(
        JSON.stringify({ error: 'Cannot simulate a live (non-test) payment.' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 403 }
      )
    }

    // Guard: only mock provider rows
    if (payment.payment_provider !== 'mock') {
      return new Response(
        JSON.stringify({ error: 'Cannot simulate a non-mock payment.' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 403 }
      )
    }

    // Guard: verify simulation token
    if (!payment.simulation_token || payment.simulation_token !== simulationToken) {
      return new Response(
        JSON.stringify({ error: 'Invalid or missing simulation token.' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 403 }
      )
    }

    // Guard: strict state transitions (PENDING is the only mutable state)
    if (payment.status !== 'PENDING') {
      return new Response(
        JSON.stringify({
          error: `Payment is no longer mutable. Current status: ${payment.status}`,
        }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 409 }
      )
    }

    // ------------------------------------------------------------------
    // 3. Apply the state transition
    // ------------------------------------------------------------------
    if (status === 'PAID') {
      // Update payment
      const { error: payError } = await supabaseClient
        .from('payments')
        .update({ status: 'PAID', paid_at: new Date().toISOString() })
        .eq('id', paymentId)

      if (payError) throw payError

      // Update order
      const { error: orderError } = await supabaseClient
        .from('orders')
        .update({ status: 'PAID' })
        .eq('id', orderId)

      if (orderError) throw orderError

      // Check for existing receipt (idempotency)
      const { data: existingReceipt } = await supabaseClient
        .from('receipts')
        .select('id')
        .eq('payment_id', paymentId)
        .maybeSingle()

      if (existingReceipt) {
        return new Response(
          JSON.stringify({ orderId, paymentId, receiptId: existingReceipt.id, status: 'PAID' }),
          { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        )
      }

      // Create receipt
      const randomPart = Math.floor(100000 + Math.random() * 900000)
      const receiptNumber = `CLC-REC-${randomPart}`

      const { data: receiptData, error: receiptError } = await supabaseClient
        .from('receipts')
        .insert({
          order_id: orderId,
          payment_id: paymentId,
          receipt_number: receiptNumber,
          is_test: true,
        })
        .select('id')
        .single()

      if (receiptError) throw receiptError

      return new Response(
        JSON.stringify({ orderId, paymentId, receiptId: receiptData.id, status: 'PAID' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      )
    } else {
      // FAILED or EXPIRED
      const targetStatus = status === 'EXPIRED' ? 'EXPIRED' : 'FAILED'

      const { error: payError } = await supabaseClient
        .from('payments')
        .update({ status: targetStatus })
        .eq('id', paymentId)

      if (payError) throw payError

      const { error: orderError } = await supabaseClient
        .from('orders')
        .update({ status: targetStatus })
        .eq('id', orderId)

      if (orderError) throw orderError

      return new Response(
        JSON.stringify({ orderId, paymentId, status: targetStatus }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      )
    }
  } catch (error) {
    console.error('Error processing mock payment:', error)
    return new Response(JSON.stringify({ error: error.message }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 400,
    })
  }
})
